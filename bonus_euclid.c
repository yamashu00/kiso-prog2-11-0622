/*
 * ★ ボーナス問題 — ユークリッドの互除法
 *
 * 【アルゴリズムの名前】ユークリッドの互除法（Euclidean Algorithm）
 * 【何が有名か】
 *   紀元前300年頃に記録された、現存する最古のアルゴリズムの1つ。
 *   分数の約分・音楽の音階・画像の解像度計算など幅広く使われている。
 *   再帰の教科書でも必ず登場する。
 *
 * 問題: 2つの整数 a = 252、b = 105 の最大公約数（GCD）を求めよ。
 *   （最大公約数 = 両方を割り切れる最大の整数）
 *
 * 出力例:
 *   GCD(252, 105) = 21
 *
 * コンパイル: gcc bonus_euclid.c -o bonus_euclid
 * 実行:       ./bonus_euclid
 *
 * ─────────────────────────────────────────────────────────
 * 【解答パターンは自由です。いくつか例を示します。】
 *
 * パターンA: 全探索
 *   1 から min(a, b) まで順に試して、両方を割り切れる最大の数を探す。
 *
 * パターンB: ユークリッドの互除法（ループ版）
 *   「a と b の GCD」は「b と (a を b で割った余り) の GCD」と等しい。
 *   余りが 0 になったとき、その時点の b が答え。
 *
 *   252 ÷ 105 = 2 余り 42  → GCD(252, 105) = GCD(105, 42)
 *   105 ÷  42 = 2 余り 21  → GCD(105,  42) = GCD( 42, 21)
 *    42 ÷  21 = 2 余り  0  → GCD( 42,  21) = 21  ✓
 *
 *   while (b != 0) {
 *       int r = a % b;
 *       a = b;
 *       b = r;
 *   }
 *   // a が答え
 *
 * パターンC: 再帰版
 *   gcd(a, b) = gcd(b, a % b)  （b が 0 なら a を返す）
 *   この1行の式をそのまま関数にできる。
 *
 * パターンD: 応用 — 最小公倍数（LCM）を求める
 *   LCM(a, b) = a × b ÷ GCD(a, b)
 *   GCD が求まれば LCM も1行で出せる。
 * ─────────────────────────────────────────────────────────
 */

// 自分の名前: 高瀬喜糸

#include <stdio.h>

int main(void) {
    int a = 252;
    int b = 105;

    /* ここに実装する */
    /* パターンA: 全探索 */
    int gcd = 1;
    int min;
    
    if (a < b) {
        min = a;
    } else {
        min = b;
    }
    // int min = (a < b) ? a : b;

    for (int i = 1; i <= min; i++) {
        if (a % i == 0 && b % i == 0) {
            gcd = i;
        }
    }

    int result = gcd;

    /* printf("GCD(%d, %d) = %d\n", 252, 105, result); */
    printf("GCD(%d, %d) = %d\n", 252, 105, result);

    return 0;
}
