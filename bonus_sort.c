/*
 * ★ ボーナス問題 — 選択ソート / バブルソート
 *
 * 【アルゴリズムの名前】選択ソート（Selection Sort）/ バブルソート（Bubble Sort）
 * 【何が有名か】
 *   プログラムで「並べ替え」といえば必ず登場する基本アルゴリズム。
 *   スマホのランキング表示・試験の点数順など、あらゆるソフトウェアで使われている。
 *
 * 問題: 以下の10個の数を小さい順に並べ替えて表示せよ。
 *
 *   int data[10] = {38, 12, 74, 5, 91, 23, 47, 60, 8, 33};
 *
 * 出力例:
 *   5 8 12 23 33 38 47 60 74 91
 *
 * コンパイル: gcc bonus_sort.c -o bonus_sort
 * 実行:       ./bonus_sort
 *
 * ─────────────────────────────────────────────────────────
 * 【解答パターンは自由です。いくつか例を示します。】
 *
 * パターンA: 選択ソート
 *   「未ソート部分の中から最小値を探して、先頭の要素と交換する」を繰り返す。
 *
 *   [38, 12, 74,  5, 91, 23, 47, 60,  8, 33]
 *     ↑最小値(5)を探して先頭と交換
 *   [ 5, 12, 74, 38, 91, 23, 47, 60,  8, 33]
 *        ↑残りの中から最小値(8)を探して2番目と交換
 *   [ 5,  8, 74, 38, 91, 23, 47, 60, 12, 33]
 *   ...
 *
 * パターンB: バブルソート
 *   「隣り合う2つを比較して、大きい方を右へ移す」を繰り返す。
 *   大きい数が泡のように右端へ浮かび上がるイメージ。
 *
 *   [38, 12, 74,  5, 91, ...]
 *    38>12 → 交換: [12, 38, 74,  5, 91, ...]
 *    38<74 → そのまま: [12, 38, 74,  5, 91, ...]
 *    74> 5 → 交換: [12, 38,  5, 74, 91, ...]
 *   ...1周で最大値が右端へ
 *
 * パターンC: 何回の比較が必要か数える
 *   10個の場合、パターンA/B それぞれで比較は合計何回になるか？
 *   n個の場合、一般式で表すとどうなるか？（n² に比例する理由を考える）
 * ─────────────────────────────────────────────────────────
 */
 /* 考察
 * 選択ソートの比較回数:
 *   i=0 のとき 9回, i=1 のとき 8回, ... , i=8 のとき 1回
 *   合計 = 9 + 8 + 7 + ... + 1 = 45回
 *
 * n個の場合:
 *   (n-1) + (n-2) + ... + 1 = n(n-1)/2 回
 *   n=10 なら 10 × 9/2 = 45回 で一致。
 *
 * なぜ n² に比例するか:
 *   n(n-1)/2 = (n² - n)/2 で、最も大きい項が n²。
 *   n が大きくなると n² の影響が支配的になるので、
 *   計算量は O(n²) と表される。
 *   （バブルソートも比較回数は同じ n(n-1)/2 = 45回）
 */

// 自分の名前: 八幡咲良

#include <stdio.h>

int main(void) {
    int data[10] = {38, 12, 74, 5, 91, 23, 47, 60, 8, 33};
    int n = 10;

    /* ここに実装する */
    //パターンA
    for (int i = 0; i < n - 1; i++) {
        int min_idx = i;                  // 最小値の位置を仮置き
        for (int j = i + 1; j < n; j++) {
            if (data[j] < data[min_idx]) {
                min_idx = j;              // より小さい値を見つけたら更新
            }
        }
        // 先頭(i番目)と最小値を交換
        int tmp = data[i];
        data[i] = data[min_idx];
        data[min_idx] = tmp;
    }

    /* 結果を表示する */
    for (int i = 0; i < n; i++) {
        printf("%d ", data[i]);
    }
    printf("\n");

    return 0;
}
