#include <stdio.h>

int main(void)
{
    int kaisu = 1;
    int saiaku[1000];
    int saiaku_count = 0;
    int answer;
    int i;
    int count = 0;
    int low = 1;
    int high = 1000;
    while (kaisu <= 1000) // 最悪のやつを求めるプログラム
    {
        answer = kaisu;
        i = 500;
        count = 0;
        low = 1;
        high = 1000;
        do
        {
            i = (low + high) / 2;
            count++;

            if (i < answer)
            {
                low = i + 1;
            }
            else if (i > answer)
            {
                high = i - 1;
            }

        } while (i != answer);
        if (count == 10)
        {
            saiaku[saiaku_count] = answer;
            saiaku_count++;
        }
        kaisu++;
    }

    printf("個数:%d\n", saiaku_count);
    for (i = 0; i < saiaku_count; i++)
    {
        printf("最悪のケース: %d\n", saiaku[i]);
    }
    return 0;
}
