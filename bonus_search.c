/*
 * ★ ボーナス問題（発展）
 *
 * 問題: 1以上1000以下の整数から1つの数（answer）を当てるシミュレーション
 *
 * ルール:
 *   - answer はプログラム内で好きな値に設定してよい
 *   - 毎回1つ数を予想し、「大きい」「小さい」「正解」のどれかを判定する
 *   - 何回の予想で当てられたかを最後に表示する
 *
 * 例（answer = 737 の場合）:
 *   予想: 500 → 小さい
 *   予想: 750 → 大きい
 *   予想: 625 → 小さい
 *   ...
 *   正解！ X回で当てました
 *
 * コンパイル: gcc bonus_search.c -o bonus
 * 実行:       ./bonus
 *
 * ─────────────────────────────────────────────────────────
 * 【解答パターンは自由です。いくつか例を示します。】
 *
 * パターンA: 1から順番に試す（線形探索）
 *   1, 2, 3, ... と順に予想する
 *   最悪のケース: 1000回
 *
 * パターンB: 常に「残りの範囲の真ん中」を予想する（二分探索）
 *   最初の予想: 500（1〜1000の中央）
 *   「大きい」なら次は 250（1〜499の中央）
 *   「小さい」なら次は 750（501〜1000の中央）
 *   → 毎回、候補が半分に絞られる
 *   最悪のケース: 何回？
 *
 * パターンC: 数学的に考える
 *   1000個の候補を毎回半分に絞ると...
 *   1000 → 500 → 250 → 125 → 63 → 32 → 16 → 8 → 4 → 2 → 1
 *   何回で1つに絞れるか数えてみよう。
 *   「2の何乗が1000以上になるか」と関係がある。
 *
 * パターンD: answer を変えて試す
 *   answer = 1, 500, 1000 など「最悪のケース」になる値はどれか。
 *   コメントに考察を書くこと。
 * ─────────────────────────────────────────────────────────
 */

// 自分の名前: 後上彰吾

#include <stdio.h>

int main(void) {
    int answer = 462;
    int inputnum;
    while(inputnum != answer){
        printf("数字を入力してください:");
        scanf("%d", &inputnum);

        if(inputnum > answer){
            printf("入力した数字の方が大きい\n");
        } else if(inputnum < answer) {
            printf("入力した数字の方が小さい\n");
        } else{
            break;
        }
    }
    
    printf("正解!\n");

    return 0;
}
