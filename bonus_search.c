/*
 * ★ ボーナス問題（発展）
 *
 * 問題: 1以上1000以下の整数から1つの数（answer）を当てるシミュレーション
 *
 * ルール:
 *   - answer はプログラム内で好きな値に設定してよい
 *   - 毎回1つ数を予想し、「大きい」「小さい」「正解」のどれかを判定する
 *   - 何回の予想で当てられたかを最後に表示する
 *
 * 例（answer = 737 の場合）:
 *   予想: 500 → 小さい
 *   予想: 750 → 大きい
 *   予想: 625 → 小さい
 *   ...
 *   正解！ X回で当てました
 *
 * コンパイル: gcc bonus_search.c -o bonus
 * 実行:       ./bonus
 *
 * ─────────────────────────────────────────────────────────
 * 【解答パターンは自由です。いくつか例を示します。】
 *
 * パターンA: 1から順番に試す（線形探索）
 *   1, 2, 3, ... と順に予想する
 *   最悪のケース: 1000回
 *
 * パターンB: 常に「残りの範囲の真ん中」を予想する（二分探索）
 *   最初の予想: 500（1〜1000の中央）
 *   「大きい」なら次は 250（1〜499の中央）
 *   「小さい」なら次は 750（501〜1000の中央）
 *   → 毎回、候補が半分に絞られる
 *   最悪のケース: 何回？
 *
 * パターンC: 数学的に考える
 *   1000個の候補を毎回半分に絞ると...
 *   1000 → 500 → 250 → 125 → 63 → 32 → 16 → 8 → 4 → 2 → 1
 *   何回で1つに絞れるか数えてみよう。
 *   「2の何乗が1000以上になるか」と関係がある。
 *
 * パターンD: answer を変えて試す
 *   answer = 1, 500, 1000 など「最悪のケース」になる値はどれか。
 *   コメントに考察を書くこと。
 * ─────────────────────────────────────────────────────────
  * 【パターンB・C: 最悪のケースの回数】
  *   候補が毎回半分になるので、1000 → 500 → 250 → ... → 1。
  *   「2の何乗が1000以上になるか」= log2(1000) ≒ 9.97。
  *   よって ⌈log2(1000)⌉ = 10。最悪でも10回で当たる。
  *   （2^10 = 1024 ≥ 1000 だから10回）
  *
  *   線形探索（パターンA）は最悪1000回なので、二分探索の方が速い。
  *
  * 【パターンD: 最悪ケースになる answer】
  *   この実装では guess = (low+high)/2（切り捨て）なので、
  *   端の値ほど絞り込みに回数がかかりやすい。
  *   実際に試すと answer = 1000 が10回かかる最悪ケースの一つ。
  *   answer = 500 付近は最初の予想に近く、少ない回数で当たる。

// 自分の名前: 八幡咲良//*/

#include <stdio.h>

int main(void) {
    int answer = 827;  /* ← 好きな値に変えてよい（1〜1000） */

    /* ここに実装する */

    int low = 1, high = 1000;
    int count = 0;

    while (low <= high) {
        int guess = (low + high) / 2;   // 残りの範囲の真ん中
        count++;

        if (guess == answer) {
            printf("予想: %d → 正解！ %d回で当てました\n", guess, count);
            break;
        } else if (guess < answer) {
            printf("予想: %d → 小さい\n", guess);
            low = guess + 1;
        } else {
            printf("予想: %d → 大きい\n", guess);
            high = guess - 1;
        }
    }

    return 0;
    
}